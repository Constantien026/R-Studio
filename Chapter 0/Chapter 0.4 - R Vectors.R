### Vectors
#create a vector with the c (short for combine) function
vec1 <- c(1, 4, 6, 7)
vec2 <- c(1:5, 10)

#or use a function
#(seq is short for sequence)
vec3 <- seq(1, 10, by=2)
vec4 <- seq(0, 50, length=11)
vec5 <- seq(1, 50, length=11)
vec6 <- 1:10
vec7 <- seq(1, 10)
vec8 <- 5:1

#non-integer sequences
vec9 <- seq(0, 100*pi, by=pi)

